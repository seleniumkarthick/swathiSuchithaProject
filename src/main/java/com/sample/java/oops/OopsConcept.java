package com.sample.java.oops;

public class OopsConcept {
	
	/*
	 * 
	 * Encapsulation
	 * 	- Wrapping of data members(methods) and variables
	 * 	- Data hiding(Access Modifier - public,private and protected)
	 * 
	 * Inheritance
	 * 	- It is a child(subclass) -> parent(super class) relationship
	 * 	- extends keyword
	 * 	- Multiple inheritance is not possible in java
	 * 	- Multilevel is possible
	 * 
	 * Polymorphism
	 * 	- All the method names are same but different number of parameter or different data type in the parameter
	 * 		Two types:
	 * 			1. method overloading - same class
	 * 			2. method overriding - different class has same method name and same parameter
	 * 
	 * Interface
	 * 	- blue print
	 * 	- implements kwyword
	 *  - multiple interface is possible
	 * 
	 * Abstract
	 * 	- both abstract method and non abstract method
	 *  - cannot create for abstract class
	 * 
	 */

}
