package com.sample.java.baiscs;

public class DataTypeAndVariables {
	
	public static void main(String[] args) {
		
		/*
		 * int - whole number
		 * float - decimal number
		 * char - 'a'
		 * String - word 
		 * boolean - true/false
		 */
		
		int x=10;
		System.out.println(x);
		
		char y='a';
		System.out.println(y);
		
		String z="Please join my meeting from your computer, tablet or smartphone";
		System.out.println(z);
		
		boolean x1=false;
		System.out.println(x1);
		
	}

}
